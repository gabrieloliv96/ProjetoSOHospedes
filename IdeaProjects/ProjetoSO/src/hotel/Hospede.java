package hotel;

public class Hospede {
    int id;
    int canal;
    int tempoTv;
    int tempoDescansar;

    // Get Set
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getCanal() {
        return canal;
    }
    public void setCanal(int canal) {
        this.canal = canal;
    }
    public int getTempoTv() {
        return tempoTv;
    }
    public void setTempoTv(int tTv) {
        this.tempoTv = tTv;
    }
    public int getTempoDescansar() {
        return tempoDescansar;
    }
    public void setTempoDescansar(int tempoDescansar) {
        this.tempoDescansar = tempoDescansar;
    }
}
